/// D = diag(A_SELF) + LAMBDA * g * g^T
fn rank1_dense_d(d: usize, g: &[f64]) -> Vec<f64> {
    let mut mat = vec![0.0f64; d * d];
    for i in 0..d {
        for j in 0..d {
            let cross = LAMBDA * g[i] * g[j];
            mat[i * d + j] = if i == j { A_SELF + cross } else { cross };
        }
    }
    mat
}

/// D = diag(A_SELF) + 0.2 * G * G^T, G generated by LCG (off-block rank 2).
fn rank2_dense_d(d: usize) -> Vec<f64> {
    let mut mat = vec![0.0f64; d * d];
    for j in 0..d { mat[j * d + j] = A_SELF; }
    let n_cols = 2usize;
    let g_cols = lcg_ic(d * n_cols, 42);
    let lam = 0.2_f64;
    for i in 0..d {
        for j in 0..d {
            let dot: f64 = (0..n_cols).map(|q| g_cols[q * d + i] * g_cols[q * d + j]).sum();
            mat[i * d + j] += lam * dot;
        }
    }
    mat
}

