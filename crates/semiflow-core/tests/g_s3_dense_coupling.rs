#[allow(dead_code)]
fn dft_r2c(x: &[f64]) -> Vec<f64> {
    let n = x.len(); let tpn = TAU / n as f64;
    let mut out = vec![0.0f64; 2 * n];
    for k in 0..n {
        let (mut re, mut im) = (0.0, 0.0);
        for j in 0..n { let a = -(tpn * (j * k) as f64); re += x[j]*a.cos(); im += x[j]*a.sin(); }
        out[2*k] = re; out[2*k+1] = im;
    }
    out
}

fn dft_c2c(x: &[f64]) -> Vec<f64> {
    let n = x.len() / 2; let tpn = TAU / n as f64;
    let mut out = vec![0.0f64; 2 * n];
    for k in 0..n {
        let (mut re, mut im) = (0.0, 0.0);
        for j in 0..n {
            let a = -(tpn * (j * k) as f64); let (c, s) = (a.cos(), a.sin());
            re += x[2*j]*c - x[2*j+1]*s; im += x[2*j]*s + x[2*j+1]*c;
        }
        out[2*k] = re; out[2*k+1] = im;
    }
    out
}

fn idft(x: &[f64]) -> Vec<f64> {
    let n = x.len() / 2; let tpn = TAU / n as f64; let inv_n = 1.0 / n as f64;
    let mut out = vec![0.0f64; 2 * n];
    for k in 0..n {
        let (mut re, mut im) = (0.0, 0.0);
        for j in 0..n {
            let a = tpn * (j * k) as f64; let (c, s) = (a.cos(), a.sin());
            re += x[2*j]*c - x[2*j+1]*s; im += x[2*j]*s + x[2*j+1]*c;
        }
        out[2*k] = re * inv_n; out[2*k+1] = im * inv_n;
    }
    out
}

fn fft_nd(u: &[f64], n: usize, d: usize) -> Vec<f64> {
    let mut cplx: Vec<f64> = u.iter().flat_map(|&v| [v, 0.0]).collect();
    for j in 0..d {
        let stride = n.pow((d - 1 - j) as u32);
        let n_before = n.pow(j as u32);
        let mut next = cplx.clone();
        for ib in 0..n_before {
            for ia in 0..stride {
                let line: Vec<f64> = (0..n).flat_map(|k| {
                    let idx = ib * n * stride + k * stride + ia;
                    [cplx[2*idx], cplx[2*idx+1]]
                }).collect();
                let tr = dft_c2c(&line);
                for k in 0..n {
                    let idx = ib * n * stride + k * stride + ia;
                    next[2*idx] = tr[2*k]; next[2*idx+1] = tr[2*k+1];
                }
            }
        }
        cplx = next;
    }
    cplx
}

fn ifft_nd(cplx: &[f64], n: usize, d: usize) -> (Vec<f64>, f64) {
    let nd = n.pow(d as u32);
    let mut cur = cplx.to_vec();
    for j in 0..d {
        let stride = n.pow((d - 1 - j) as u32);
        let n_before = n.pow(j as u32);
        let mut next = cur.clone();
        for ib in 0..n_before {
            for ia in 0..stride {
                let line: Vec<f64> = (0..n).flat_map(|k| {
                    let idx = ib * n * stride + k * stride + ia;
                    [cur[2*idx], cur[2*idx+1]]
                }).collect();
                let iv = idft(&line);
                for k in 0..n {
                    let idx = ib * n * stride + k * stride + ia;
                    next[2*idx] = iv[2*k]; next[2*idx+1] = iv[2*k+1];
                }
            }
        }
        cur = next;
    }
    let mut out = vec![0.0f64; nd]; let mut max_imag = 0.0f64;
    for i in 0..nd {
        out[i] = cur[2*i];
        let im = cur[2*i+1].abs(); if im > max_imag { max_imag = im; }
    }
    (out, max_imag)
}

/// Build d-D complex expsym from symbol formula (independent re-impl).
fn build_expsym_nd(n: usize, d: usize, d_mat: &[f64], b: &[f64], dx: f64, tau: f64) -> Vec<f64> {
    let sd2: Vec<f64> = (0..n).map(|m| (2.0*(TAU*m as f64/n as f64).cos()-2.0)/(dx*dx)).collect();
    let sd1r: Vec<f64> = (0..n).map(|m| (TAU*m as f64/n as f64).sin()/dx).collect();
    let nd = n.pow(d as u32);
    let mut out = vec![0.0f64; 2 * nd];
    for flat in 0..nd {
        let mut f = flat; let mut modes = vec![0usize; d];
        for j in (0..d).rev() { modes[j] = f % n; f /= n; }
        let mut sym_re = 0.0f64;
        for j in 0..d { sym_re += d_mat[j*d+j] * sd2[modes[j]]; }
        for j in 0..d { for k in (j+1)..d { sym_re -= 2.0*d_mat[j*d+k]*sd1r[modes[j]]*sd1r[modes[k]]; } }
        let sym_im: f64 = (0..d).map(|j| b[j] * sd1r[modes[j]]).sum();
        let exp_re = (tau * sym_re).exp(); let phase = tau * sym_im;
        out[2*flat] = exp_re*phase.cos(); out[2*flat+1] = exp_re*phase.sin();
    }
    out
}

/// Apply the d-D spectral evolver (local re-impl, zero production reuse).
fn spectral_evolve(u0: &[f64], n: usize, d: usize, d_mat: &[f64], b: &[f64], tau: f64) -> (Vec<f64>, f64) {
    let dx = 1.0 / n as f64;
    let mut cplx = fft_nd(u0, n, d);
    let expsym = build_expsym_nd(n, d, d_mat, b, dx, tau);
    let nd = u0.len();
    for i in 0..nd {
        let (fr, fi) = (cplx[2*i], cplx[2*i+1]);
        let (er, ei) = (expsym[2*i], expsym[2*i+1]);
        cplx[2*i] = fr*er - fi*ei; cplx[2*i+1] = fr*ei + fi*er;
    }
    ifft_nd(&cplx, n, d)
}

