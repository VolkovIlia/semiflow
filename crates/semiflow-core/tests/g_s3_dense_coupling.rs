/// Coupling eigenvector: g = cos(linspace(0.3, 1.4, d)) * 0.6
fn gate_g(d: usize) -> Vec<f64> {
    (0..d)
        .map(|k| {
            let span = if d > 1 { 1.1 / (d as f64 - 1.0) } else { 0.0 };
            let t = 0.3 + span * k as f64;
            t.cos() * 0.6
        })
        .collect()
}

/// Gate drift vector: b[j] = 0.6 + 0.1·j
fn gate_b(d: usize) -> Vec<f64> {
    (0..d).map(|j| 0.6 + 0.1 * j as f64).collect()
}

