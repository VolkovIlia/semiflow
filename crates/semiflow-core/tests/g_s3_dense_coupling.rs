fn rel_l2(a: &[f64], b: &[f64]) -> f64 {
    let (num, den) = a.iter().zip(b.iter()).fold((0.0, 0.0), |(n, d), (ai, bi)| {
        (n + (ai-bi)*(ai-bi), d + bi*bi)
    });
    if den < 1e-300 { num.sqrt() } else { (num/den).sqrt() }
}

fn lcg_ic(count: usize, seed: u64) -> Vec<f64> {
    let mut state = seed;
    (0..count).map(|_| {
        state = state.wrapping_mul(6_364_136_223_846_793_005).wrapping_add(1_442_695_040_888_963_407);
        let bits = (state >> 11) as f64;
        bits / (1u64 << 53) as f64 * 2.0 - 1.0
    }).collect()
}

fn smooth_ic(n: usize, d: usize) -> Vec<f64> {
    let dx2 = (2.0 * core::f64::consts::PI) / n as f64;
    let g: Vec<f64> = (0..n).map(|k| (k as f64 * dx2).cos() + 0.3).collect();
    let nd = n.pow(d as u32);
    (0..nd).map(|flat| {
        let mut f = flat; let mut val = 1.0f64;
        for _ in 0..d { val *= g[f % n]; f /= n; }
        val
    }).collect()
}

fn gaussian_ic(n: usize, d: usize) -> Vec<f64> {
    let nd = n.pow(d as u32);
    (0..nd).map(|flat| {
        let mut f = flat; let mut xs = 0.0f64;
        for _ in 0..d { let k = f % n; f /= n; xs += ((k as f64 + 0.5)/n as f64 - 0.5).powi(2); }
        (-xs / 0.05).exp()
    }).collect()
}

fn rank_rect_qr(a: &[f64], rows: usize, cols: usize, eps: f64) -> usize {
    let mut r = a.to_vec();
    let k = rows.min(cols);
    let mut col_norms: Vec<f64> = (0..cols)
        .map(|j| (0..rows).map(|i| r[i*cols+j]*r[i*cols+j]).sum::<f64>().sqrt())
        .collect();
    let max_norm = col_norms.iter().cloned().fold(0.0f64, f64::max);
    let tol = eps * max_norm;
    let mut rank = 0usize;
    for col in 0..k {
        let (pivot, pnorm) = col_norms[col..].iter().enumerate()
            .max_by(|a, b| a.1.partial_cmp(b.1).unwrap())
            .map(|(i, &v)| (i+col, v)).unwrap_or((col, 0.0));
        if pnorm < tol { break; }
        rank += 1;
        if pivot != col {
            for row in 0..rows { r.swap(row*cols+col, row*cols+pivot); }
            col_norms.swap(col, pivot);
        }
        let norm = (col..rows).map(|i| r[i*cols+col]*r[i*cols+col]).sum::<f64>().sqrt();
        if norm < 1e-300 { break; }
        let sign = if r[col*cols+col] >= 0.0 { 1.0 } else { -1.0 };
        r[col*cols+col] += sign * norm;
        let inv = 1.0 / (col..rows).map(|i| r[i*cols+col]*r[i*cols+col]).sum::<f64>().sqrt();
        for i in col..rows { r[i*cols+col] *= inv; }
        for j in (col+1)..cols {
            let dot: f64 = (col..rows).map(|i| r[i*cols+col]*r[i*cols+j]).sum();
            for i in col..rows { r[i*cols+j] -= 2.0*dot*r[i*cols+col]; }
        }
        for j in (col+1)..cols {
            col_norms[j] = (col..rows).map(|i| r[i*cols+j]*r[i*cols+j]).sum::<f64>().sqrt();
        }
    }
    rank
}

/// Max TT bond rank for a REAL tensor via half-cut QR (used only for Assert 7).
fn max_bond_rank(u: &[f64], n: usize, d: usize, eps: f64) -> usize {
    let left_n = n; let right_n = n.pow((d - 1) as u32);
    let rk1 = if left_n <= right_n { rank_rect_qr(u, left_n, right_n, eps) } else {
        let mut at = vec![0.0f64; right_n * left_n];
        for i in 0..left_n { for j in 0..right_n { at[j*left_n+i] = u[i*right_n+j]; } }
        rank_rect_qr(&at, right_n, left_n, eps)
    };
    rk1
}

/// Power-deflation: find top-k singular values of real m×n matrix B (m rows, n cols).
/// Iterates 20 steps per value; returns values in decreasing order until < stop_at.
/// Uses left power iteration: u = B v / ||B v||, v = B^T u; sigma = ||B^T u||.
fn top_k_sv_deflation(b: &mut [f64], m: usize, n: usize, k: usize, stop_at: f64) -> Vec<f64> {
    let mut sigs = Vec::with_capacity(k);
    for _ in 0..k {
        // Initial u: row-sum direction
        let mut u: Vec<f64> = (0..m)
            .map(|i| (0..n).map(|j| b[i*n+j]).sum::<f64>())
            .collect();
        let un = u.iter().map(|x| x*x).sum::<f64>().sqrt();
        if un < 1e-300 { break; }
        for x in &mut u { *x /= un; }
        for _ in 0..22 {
            // v = B^T u
            let v: Vec<f64> = (0..n).map(|j| (0..m).map(|i| b[i*n+j]*u[i]).sum()).collect();
            // u_new = B v
            let mut u2: Vec<f64> = (0..m).map(|i| (0..n).map(|j| b[i*n+j]*v[j]).sum()).collect();
            let un2 = u2.iter().map(|x| x*x).sum::<f64>().sqrt();
            if un2 < 1e-300 { break; }
            for x in &mut u2 { *x /= un2; }
            u = u2;
        }
        // sigma = ||B^T u||
        let v: Vec<f64> = (0..n).map(|j| (0..m).map(|i| b[i*n+j]*u[i]).sum()).collect();
        let sigma = v.iter().map(|x| x*x).sum::<f64>().sqrt();
        if sigma <= stop_at { break; }
        sigs.push(sigma);
        // Deflate: B -= u * v^T (where v = B^T u has magnitude sigma, so B -= sigma * u * v_hat^T)
        for i in 0..m {
            for j in 0..n { b[i*n+j] -= u[i] * v[j]; }
        }
    }
    sigs
}

/// Build block-real matrix B = [[Re(M),-Im(M)];[Im(M),Re(M)]] oriented so rows ≤ cols.
/// M has shape left × right (complex). Returns (B, rows, cols) where rows = 2*min(left,right).
fn build_block_b(t_re: &[f64], t_im: &[f64], left: usize, right: usize)
    -> (Vec<f64>, usize, usize)
{
    if left <= right {
        // B: 2*left rows × 2*right cols
        let (m, nc) = (2*left, 2*right);
        let mut b = vec![0.0f64; m * nc];
        for i in 0..left {
            for j in 0..right {
                let v_re = t_re[i*right+j]; let v_im = t_im[i*right+j];
                b[i*nc+j] = v_re;           b[i*nc+(j+right)] = -v_im;
                b[(i+left)*nc+j] = v_im;    b[(i+left)*nc+(j+right)] = v_re;
            }
        }
        (b, m, nc)
    } else {
        // Transpose: B^T would have 2*right rows × 2*left cols
        let (m, nc) = (2*right, 2*left);
        let mut b = vec![0.0f64; m * nc];
        for i in 0..left {
            for j in 0..right {
                let v_re = t_re[i*right+j]; let v_im = t_im[i*right+j];
                // B^T[j, i] = Re[i,j], B^T[j+right, i+left] = Re[i,j] (same block structure)
                b[j*nc+i] = v_re;            b[j*nc+(i+left)] = v_im;
                b[(j+right)*nc+i] = -v_im;   b[(j+right)*nc+(i+left)] = v_re;
            }
        }
        (b, m, nc)
    }
}

/// Max-over-ALL-bonds TT-SVD rank of d-D complex tensor stored as interleaved (re,im).
///
/// Full left-to-right cut sweep (NOT a single half-cut). Mirrors Python `tt_ranks → max(rks)`.
/// Uses block-real embedding B = [[Re,-Im];[Im,Re]] at each cut; rank_C(M,eps) = count(σ>ε·σ_max)/2.
/// SVD via power deflation (no gram matrix — avoids precision loss from B B^T formation).
fn max_all_bonds_tt_rank(tensor_reim: &[f64], n: usize, d: usize, eps: f64) -> usize {
    let nd = n.pow(d as u32);
    let t_re: Vec<f64> = (0..nd).map(|i| tensor_reim[2*i]).collect();
    let t_im: Vec<f64> = (0..nd).map(|i| tensor_reim[2*i+1]).collect();
    let mut max_rk = 0usize;
    for k in 1..d {
        let left = n.pow(k as u32); let right = n.pow((d-k) as u32);
        let (b, m, nc) = build_block_b(&t_re, &t_im, left, right);
        // Find sigma_max first (k=1, no early stop)
        let sigs_probe = top_k_sv_deflation(&mut b.clone(), m, nc, 1, 0.0);
        if sigs_probe.is_empty() { continue; }
        let sigma_max = sigs_probe[0];
        let stop_at = eps * sigma_max;
        // Count all real SVs above stop_at: up to 2*(n+1) per complex rank bound
        // The block-real embedding has singular values in pairs; max rank = min(left,right) ≤ n^4
        let k_max = (2 * (left.min(right) + 1)).min(m);
        let (mut b2, _, _) = build_block_b(&t_re, &t_im, left, right);
        let sigs = top_k_sv_deflation(&mut b2, m, nc, k_max, stop_at);
        let rk = sigs.len() / 2; // block-real pairs → complex rank
        if rk > max_rk { max_rk = rk; }
    }
    max_rk
}

