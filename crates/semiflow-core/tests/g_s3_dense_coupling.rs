fn mat_vec_l(a: &[f64], v: &[f64], m: usize) -> Vec<f64> {
    (0..m).map(|i| (0..m).map(|j| a[i * m + j] * v[j]).sum()).collect()
}

fn mat_mat_l(a: &[f64], b: &[f64], m: usize) -> Vec<f64> {
    let mut c = vec![0.0f64; m * m];
    for i in 0..m {
        for k in 0..m {
            if a[i * m + k] == 0.0 { continue; }
            for j in 0..m { c[i * m + j] += a[i * m + k] * b[k * m + j]; }
        }
    }
    c
}

fn mat_eye_l(m: usize) -> Vec<f64> {
    let mut e = vec![0.0f64; m * m];
    for i in 0..m { e[i * m + i] = 1.0; }
    e
}

fn mat_inf_l(a: &[f64], m: usize) -> f64 {
    (0..m).map(|i| (0..m).map(|j| a[i * m + j].abs()).sum::<f64>()).fold(0.0, f64::max)
}

fn lu_factor_l(a: &mut [f64], m: usize) -> Vec<usize> {
    let mut piv: Vec<usize> = (0..m).collect();
    for col in 0..m {
        let mut mx = a[col * m + col].abs(); let mut mr = col;
        for row in (col + 1)..m { let v = a[row * m + col].abs(); if v > mx { mx = v; mr = row; } }
        if mr != col { for j in 0..m { a.swap(col * m + j, mr * m + j); } piv.swap(col, mr); }
        let pv = a[col * m + col]; if pv.abs() < 1e-300 { continue; }
        let inv = 1.0 / pv;
        for row in (col + 1)..m {
            let f = a[row * m + col] * inv; a[row * m + col] = f;
            for j in (col + 1)..m { let acj = a[col * m + j]; a[row * m + j] -= f * acj; }
        }
    }
    piv
}

fn lu_solve_l(a: &[f64], piv: &[usize], b: &mut [f64], m: usize) {
    let tmp = b.to_vec();
    for i in 0..m { b[i] = tmp[piv[i]]; }
    for col in 0..m { for row in (col + 1)..m { b[row] -= a[row * m + col] * b[col]; } }
    for row in (0..m).rev() {
        for col in (row + 1)..m { b[row] -= a[row * m + col] * b[col]; }
        let d = a[row * m + row]; if d.abs() > 1e-300 { b[row] /= d; }
    }
}

/// Padé[6/6] + scaling-squaring expm (local, zero production reuse).
fn expm_l(a: &[f64], m: usize) -> Vec<f64> {
    let norm = mat_inf_l(a, m);
    let mut s = 0u32; let mut thr = 0.5_f64;
    while norm > thr && s < 30 { s += 1; thr *= 2.0; }
    let sc = 0.5_f64.powi(s as i32);
    let mut as_ = a.to_vec(); for v in &mut as_ { *v *= sc; }
    let c = [1.0, 0.5, 5.0/44.0, 1.0/66.0, 1.0/792.0, 1.0/15840.0, 1.0/665280.0];
    let a2 = mat_mat_l(&as_, &as_, m); let a4 = mat_mat_l(&a2, &a2, m); let a6 = mat_mat_l(&a2, &a4, m);
    let eye = mat_eye_l(m);
    let blend = |coeffs: &[(usize, f64)]| -> Vec<f64> {
        let mut acc = vec![0.0f64; m * m];
        for &(k, ck) in coeffs {
            let src: &[f64] = match k { 0 => &eye, 2 => &a2, 4 => &a4, _ => &a6 };
            for (av, &sv) in acc.iter_mut().zip(src.iter()) { *av += ck * sv; }
        }
        acc
    };
    let v = blend(&[(0, c[0]), (2, c[2]), (4, c[4]), (6, c[6])]);
    let inner = blend(&[(0, c[1]), (2, c[3]), (4, c[5])]);
    let u = mat_mat_l(&as_, &inner, m);
    let mut p = v.clone(); for (pi, &ui) in p.iter_mut().zip(u.iter()) { *pi += ui; }
    let mut q = v; for (qi, &ui) in q.iter_mut().zip(u.iter()) { *qi -= ui; }
    let piv = lu_factor_l(&mut q, m);
    let mut exp_s = vec![0.0f64; m * m];
    for col in 0..m {
        let mut rhs: Vec<f64> = (0..m).map(|row| p[row * m + col]).collect();
        lu_solve_l(&q, &piv, &mut rhs, m);
        for row in 0..m { exp_s[row * m + col] = rhs[row]; }
    }
    for _ in 0..s { exp_s = mat_mat_l(&exp_s, &exp_s, m); }
    exp_s
}

/// Add D2_j term to dense generator.
fn add_d2_axis(l: &mut [f64], n: usize, d: usize, j: usize, coeff: f64, dx: f64) {
    let tot = n.pow(d as u32);
    let s = n.pow((d - 1 - j) as u32);
    let dx2 = dx * dx;
    for idx in 0..tot {
        let ij = (idx / s) % n;
        let base = idx - ij * s;
        let ip = base + ((ij + 1) % n) * s;
        let im = base + ((ij + n - 1) % n) * s;
        l[idx * tot + idx] += coeff * (-2.0) / dx2;
        l[idx * tot + ip] += coeff / dx2;
        l[idx * tot + im] += coeff / dx2;
    }
}

/// Add D1c_j term to dense generator.
fn add_d1c_axis(l: &mut [f64], n: usize, d: usize, j: usize, coeff: f64, dx: f64) {
    let tot = n.pow(d as u32);
    let s = n.pow((d - 1 - j) as u32);
    let two_dx = 2.0 * dx;
    for idx in 0..tot {
        let ij = (idx / s) % n;
        let base = idx - ij * s;
        let ip = base + ((ij + 1) % n) * s;
        let im = base + ((ij + n - 1) % n) * s;
        l[idx * tot + ip] += coeff / two_dx;
        l[idx * tot + im] -= coeff / two_dx;
    }
}

/// Add `2·D[j,k]·D1c_j·D1c_k` for an ARBITRARY pair j<k (not just adjacent).
fn add_d1c_pair(l: &mut [f64], n: usize, d: usize, j: usize, k: usize, coeff: f64, dx: f64) {
    debug_assert!(j < k);
    let tot = n.pow(d as u32);
    let sj = n.pow((d - 1 - j) as u32);
    let sk = n.pow((d - 1 - k) as u32);
    let four_dx2 = 4.0 * dx * dx;
    for ri in 0..tot {
        let ij = (ri / sj) % n;
        let ik = (ri / sk) % n;
        for (dj, cj_sign) in [(1usize, 1.0f64), (n - 1, -1.0f64)] {
            let ij_new = (ij + dj) % n;
            let col_j = ri - ij * sj + ij_new * sj;
            for (dk, ck_sign) in [(1usize, 1.0f64), (n - 1, -1.0f64)] {
                let ik_new = (ik + dk) % n;
                let col = col_j - ik * sk + ik_new * sk;
                l[ri * tot + col] += coeff * cj_sign * ck_sign / four_dx2;
            }
        }
    }
}

/// Build `n^d × n^d` centred-FD generator for DENSE D and drift b (all pairs j<k).
fn build_gen_dense(n: usize, d: usize, d_mat: &[f64], b: &[f64], dx: f64) -> Vec<f64> {
    let tot = n.pow(d as u32);
    let mut l = vec![0.0f64; tot * tot];
    for j in 0..d {
        add_d2_axis(&mut l, n, d, j, d_mat[j * d + j], dx);
        add_d1c_axis(&mut l, n, d, j, b[j], dx);
    }
    for j in 0..d { for k in (j + 1)..d { add_d1c_pair(&mut l, n, d, j, k, 2.0 * d_mat[j * d + k], dx); } }
    l
}

